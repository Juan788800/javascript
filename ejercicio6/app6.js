/*6. Calcular perímetro de un cuadrado
• Solicita la longitud del lado.
• Calcula el perímetro y muéstralo.
*/

let longitud_lado = parseFloat(prompt("ingrese la longitud del lado para calcular el perimetro del cuadrado"))


formular_perimetro = longitud_lado * 4 ;

perimetro = formular_perimetro;

alert("el perimetro del cuadrado es : " + perimetro);