/*7. Calcular el doble y el triple
• Solicita un número.
• Muestra su doble y su triple.
*/

let numero = parseFloat(prompt("Ingrese un número por favor: "));

let doble = numero * 2;
let triple = numero * 3;

alert("El doble de " + numero + " es: " + doble);
alert("El triple de " + numero + " es: " + triple);